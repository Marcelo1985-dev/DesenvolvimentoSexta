
//definir o modelo de cliente dentro da apllicação
public class Cliente {
	
	private String nome;
	private String cpf;
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getNome() {
		return this.nome;
	}
	
	public void SetCpf(String cpf) {
		this.cpf = cpf;
	}
	
	public String getCpf() {
		return this.cpf;
	}
	
	
	

}
